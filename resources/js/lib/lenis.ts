import Lenis from '@studio-freight/lenis';

let lenis: Lenis | null = null;

export function initLenis() {
    if (lenis) return lenis;

    lenis = new Lenis();

    function raf(time: number) {
        lenis?.raf(time);
        requestAnimationFrame(raf);
    }

    requestAnimationFrame(raf);

    return lenis;
}
